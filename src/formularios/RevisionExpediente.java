    public void cargarDatos() {
        String[] nombreColumnas = {"Id", "Nombres", "Apellidos"};
        DefaultTableModel modelo = new DefaultTableModel(null, nombreColumnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        DatoControlador datoControlador = new DatoControlador();
        List<Dato> listaDatos = datoControlador.listar();
        
        for (Dato dato : listaDatos) {
            Object[] data = new Object[nombreColumnas.length];
            data[0] = dato.getId();
            data[1] = dato.getPrimerNombre() + " " + dato.getSegundoNombre();
            data[2] = dato.getPrimerApellido() + " " + dato.getSegundoApellido();
            
            modelo.addRow(data);
        }
        tablaExpediente.setModel(modelo);
    }
    
