    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tablaExpediente.getSelectedRow();
        
        if (fila != -1) {
            int id = Integer.parseInt(lblId.getText().toString());
            String nombre = tablaExpediente.getValueAt(fila, 1).toString();
            
            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea eliminar el paciente id: " + id + ", " + nombre + " ?", "Confirmar acción", JOptionPane.WARNING_MESSAGE);
            
            if (confirmacion == 0) {
                DatoControlador datoControlador = new DatoControlador();
                boolean respuesta = datoControlador.eliminar(id);
                
                if (respuesta) {
                    JOptionPane.showMessageDialog(null, "Se eliminó correctamente la información del paciente " + id + ", " + nombre, "Registro eliminado", JOptionPane.INFORMATION_MESSAGE);
                    
                    cargarDatos();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Acción cancelada", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

